        keyboardActions = keyboardActions,
        singleLine = singleLine,
        maxLines = maxLines,
        minLines = minLines,
        interactionSource = interactionSource,
        shape = shape,
        colors = colors,
    )
}

@DPComponentPreview
@Composable
private fun DPTextFieldsPreview() {
    Preview {
        val filled = remember { mutableStateOf("") }
        val outlined = remember { mutableStateOf("") }
        Column(
            modifier = Modifier.padding(16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp),
        ) {
            DPFilledTextField(
                value = filled.value,
                onValueChange = { filled.value = it },
                label = { Text("Filled") },
            )
            DPOutlinedTextField(
                value = outlined.value,
                onValueChange = { outlined.value = it },
                label = { Text("Outlined") },
            )
        }
    }
}
